package j05070_conglacbobongda2;

public class DoiBong {
    private String ma,ten;
    private int gv;

    public DoiBong(String ma, String ten, int gv) {
        this.ma = ma;
        this.ten = ten;
        this.gv = gv;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }

    public int getGv() {
        return gv;
    }

}
